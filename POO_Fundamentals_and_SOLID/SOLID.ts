//Principios para codigo mantenible, escalable y robusto
//Alta coheción (Colaboración entre clases) y bajo acoplamiento (Evitar que una clase dependa fuertemente de otra clase)

//S(Single responsability): Una clase debe tener una razón para existir y no para cambiar
//O(Open/Closed): Las piezas/clases del software deben estar abiertas a extensión y cerradas a modificación 
//L(Liskov): Las clases hijas deberian ser capacez de ser remplazables por sus padres 
//I(Interface Segregation): Implementar mas interfaces sencillas es mejor que implementar una sola robusta (Dividir para ganar xd)
//D(Dependency Inversion): Clases de alto nivel no deben depender de clases de bajo nivel